//SUMA
public class PrimerPrograma{
	public static void main(String args []){
		int operadorUno;//DECLARACION DE VARIABLE
			operadorUno = 0;//INICIALIZACION DE VARIABLE
		int operadorDos = 0;
		int resultado = 0;
		
			//ENTRADA
			operadorUno = 2;
			operadorDos = 3;
			
			//PROCESO
			resultado = operadorUno + operadorDos;
			
			//SALIDA
			System.out.print("El resultado de la suma es: " + resultado);
		
	}
	
}